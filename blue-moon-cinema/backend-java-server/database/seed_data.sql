begin transaction;

drop table if exists movies, showtimes, transactionhistory, theaters cascade;

create table movies (
	movie_id serial,
	title varchar(100) not null,
	genre varchar(20) not null,
	description varchar(500) not null,
	rating varchar(10),
	runtime int,
	poster_img varchar(100),
	constraint PK_movie primary key (movie_id)
);

create table showtimes(
	showtime_id serial,
	movie_id int not null,
	show_date date not null,
	show_time time not null,
	seating_number varchar(150),
	constraint PK_showtime primary key (showtime_id),
	constraint FK_movie_id foreign key (movie_id) references movies(movie_id)
);

create table theaters(
	theater_id serial,
	showtime_id int not null,
	movie_id int not null,
	num_seats int not null,
	constraint FK_movie_id foreign key (movie_id) references movies(movie_id),
	constraint fk_showtime_id foreign key (showtime_id) references showtimes(showtime_id)
);


create table transactionhistory(
	reservation_id serial,
	movie_id int not null,
	user_id int not null,
	showtime_id int not null,
	num_tickets int not null,
	customer_name varchar(100) not null,
	billing_address varchar(500) not null,
	zipcode numeric(5,0) not null,
	email_address varchar(50) not null,
	total_cost decimal(4,2) not null,
	seating_number varchar(150),
	reserve_time timestamp default now(), 
	constraint pk_reservation_id primary key (reservation_id),
	constraint FK_movie_id foreign key (movie_id) references movies(movie_id),
	constraint fk_showtime_id foreign key (showtime_id) references showtimes(showtime_id),
	constraint fk_user_id foreign key (user_id) references users(user_id)
);


